package entidades;


/**
 *
 * @author Ramon Valencia 
 */
public class Asiento {

    private String numero;

    public Asiento() {
    }

    public Asiento(String numero) {
        this.numero = numero;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    @Override
    public String toString() {
        return "Asiento{"
                + ", numero=" + numero
                + '}';
    }
}
